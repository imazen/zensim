#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn render_empty_string() {
        let (buf, w, h) = render_text("", [255; 4], [0; 4]);
        assert_eq!(w, 0);
        assert_eq!(h, 0);
        assert!(buf.is_empty());
    }

    #[test]
    fn render_single_char() {
        let (buf, w, h) = render_text("A", [255, 255, 255, 255], [0, 0, 0, 255]);
        assert_eq!(w, GLYPH_W);
        assert_eq!(h, GLYPH_H);
        assert_eq!(buf.len(), (w * h * 4) as usize);
        // 'A' should have some white pixels (not all background)
        let white_count = buf.chunks(4).filter(|px| px[0] == 255).count();
        assert!(white_count > 5, "A glyph should have lit pixels, got {white_count}");
    }

    #[test]
    fn render_multiline() {
        let (buf, w, h) = render_text("AB\nCD", [255; 4], [0; 4]);
        assert_eq!(w, 2 * GLYPH_W);
        assert_eq!(h, 2 * GLYPH_H);
        assert_eq!(buf.len(), (w * h * 4) as usize);
    }

    #[test]
    fn render_stats_line() {
        let text = "score:87.2 delta:[12,8,3] 34.2%";
        let (buf, w, h) = render_text(text, [255; 4], [0; 4]);
        assert_eq!(w, text.len() as u32 * GLYPH_W);
        assert_eq!(h, GLYPH_H);
        assert_eq!(buf.len(), (w * h * 4) as usize);
    }

    #[test]
    fn font_data_count() {
        assert_eq!(FONT_DATA.len(), 95); // 0x20..=0x7E
    }

    #[test]
    fn digits_have_pixels() {
        for d in b'0'..=b'9' {
            let glyph = glyph_data(d as char);
            let bit_count: u32 = glyph.iter().map(|b| b.count_ones()).sum();
            assert!(bit_count > 3, "digit '{}' should have pixels, got {bit_count}", d as char);
        }
    }
}

// ─── Crypto grouping ────────────────────────────────────────────────────

/// Controls how crypto tokens (PCLMUL, AES, SHA) participate in SIMD
/// permutation testing.
///
/// On x86, crypto tokens are independent from compute tiers (AVX2, FMA).
/// Image processing code rarely uses crypto instructions, so toggling them
/// combinatorially with compute tiers multiplies test count without finding
/// real bugs.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum CryptoGrouping {
    /// Full combinatorial permutations of all tokens including crypto.
    /// Can be 2-4x more permutations than necessary for non-crypto code.
    /// Use when your code actually uses PCLMUL/AES/SHA instructions.
    Combinatorial,
    /// Exclude crypto tokens entirely. They stay at their natural state
    /// (enabled if the CPU has them) and don't participate in permutations.
    /// Best for image processing code that never uses PCLMUL/AES/SHA.
    Skip,
    /// Crypto tokens move as a single group: all enabled or all disabled,
    /// tested as a separate series from compute tiers. Produces
    /// `compute_perms + 1` instead of `compute_perms × crypto_perms`.
    /// Default — covers crypto without combinatorial explosion.
    #[default]
    Clump,
}

/// Token names that are crypto-only (not compute).
const CRYPTO_TOKEN_NAMES: &[&str] = &[
    "x86-64-crypto",    // X64CryptoToken (PCLMUL + AES)
    "x86-64-v3-crypto", // X64V3CryptoToken
    "aarch64-aes",      // NeonAesToken
    "aarch64-sha3",     // NeonSha3Token
];

fn is_crypto_token(name: &str) -> bool {
    CRYPTO_TOKEN_NAMES.contains(&name)
}

// ─── Report types ──────────────────────────────────────────────────────

/// Comparison of one SIMD tier against the reference (highest-tier) output.
#[derive(Debug, Clone)]
pub struct TierComparison {
    /// Human-readable tier label (e.g., "x86-64-v3 disabled").
    pub tier_label: String,
    /// Regression report comparing this tier's output against the reference.
    pub report: RegressionReport,
}

/// Result of running all SIMD tier permutations.
#[derive(Debug, Clone)]
pub struct SimdConsistencyReport {
    /// Label of the reference tier (first permutation, usually "all enabled").
    pub reference_tier: String,
    /// Total number of permutations executed.
    pub permutation_count: usize,
    /// Comparison of each non-reference tier against the reference.
    pub comparisons: Vec<TierComparison>,
    /// Whether all tier comparisons passed the tolerance.
    pub all_passed: bool,
    /// Underlying archmage permutation report (warnings, count).
    pub permutation_report: PermutationReport,
}

impl SimdConsistencyReport {
    /// Create a report for the degenerate case of a single permutation.
    fn single_tier(perm_report: PermutationReport) -> Self {
        Self {
            reference_tier: "all enabled".to_string(),
            permutation_count: 1,
            comparisons: Vec::new(),
            all_passed: true,
            permutation_report: perm_report,
        }
    }
}

impl fmt::Display for SimdConsistencyReport {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        let status = if self.all_passed { "PASS" } else { "FAIL" };

        writeln!(
            f,
            "{status}: SIMD consistency across {} permutations (ref: {}).",
            self.permutation_count, self.reference_tier,
        )?;

        for cmp in &self.comparisons {
            let tier_status = if cmp.report.passed() { " " } else { "x" };
            writeln!(
                f,
                "  {tier_status} {}: score={:.1}, max_delta={:?}, category={:?}",
                cmp.tier_label,
                cmp.report.score(),
                cmp.report.max_channel_delta(),
                cmp.report.category(),
            )?;
        }

        for w in &self.permutation_report.warnings {
            writeln!(f, "  warning: {w}")?;
        }

        Ok(())
    }
}


//! SIMD consistency testing via archmage token permutations.
//!
//! Runs an image operation under every available SIMD tier (AVX-512, AVX2,
//! SSE2, NEON, scalar) and verifies all tiers produce equivalent output.
//! Catches vectorization bugs, accumulator ordering differences, and
//! NaN-handling divergence across SIMD implementations.
//!
//! Wraps [`archmage::testing::for_each_token_permutation()`] with
//! zensim-regress comparison, forensic diff output, and optional
//! `.checksums` integration.
//!
//! Requires the `archmage` feature.
//!
//! # Which zen crates should use this?
//!
//! Any crate using `#[arcane]`, `#[autoversion]`, or `#[rite]`:
//! zenresize, zenfilters, zenpixels-convert, linear-srgb, zenjpeg,
//! zenwebp, zengif, zenpng, zenjxl-decoder, fast-ssim2, zensim.
//!
//! # Example
//!
//! ```rust,ignore
//! use zensim_regress::simd::*;
//! use zensim_regress::RegressionTolerance;
//! use archmage::testing::CompileTimePolicy;
//!
//! #[test]
//! fn resize_simd_consistency() {
//!     let input = load_test_image();
//!
//!     let report = check_simd_consistency(
//!         || {
//!             let output = resize(&input, 256, 256, Filter::Lanczos3);
//!             (output.to_rgba8(), 256, 256)
//!         },
//!         &RegressionTolerance::off_by_one(),
//!         CompileTimePolicy::Warn,
//!     ).unwrap();
//!
//!     assert!(report.all_passed, "{report}");
//! }
//! ```
//!
//! # Idiomatic patterns
//!
//! ## Call the dispatcher, not the suffixed variant
//!
//! The operation closure should call your **public dispatcher** — the function
//! that internally does `incant!` or was generated by `#[autoversion]`. Do NOT
//! call a specific tier variant like `process_v3()`:
//!
//! ```rust,ignore
//! // GOOD: calls the dispatcher — dispatch falls back as tokens are disabled
//! check_simd_consistency(
//!     || {
//!         let output = my_resize(&input, 256, 256);  // dispatches internally
//!         (output.to_rgba8(), 256, 256)
//!     },
//!     &RegressionTolerance::off_by_one(),
//!     CompileTimePolicy::Warn,
//! );
//!
//! // BAD: calls a specific tier — won't test fallback paths
//! check_simd_consistency(
//!     || {
//!         let t = X64V3Token::summon().unwrap();
//!         let output = my_resize_v3(t, &input, 256, 256);  // always V3
//!         (output.to_rgba8(), 256, 256)
//!     },
//!     ...
//! );
//! ```
//!
//! `for_each_token_permutation` disables tokens at the process level, so when
//! V3 is disabled, `X64V3Token::summon()` returns `None` and the dispatcher
//! falls through to V2 or scalar. This is how you test all tiers without
//! calling each one explicitly.
//!
//! ## Functions with token parameters
//!
//! If the function takes an explicit token (`#[arcane]` inner functions),
//! tokens are `Copy` — summon outside and capture, or use `incant!`:
//!
//! ```rust,ignore
//! use archmage::incant;
//!
//! check_simd_consistency(
//!     || {
//!         // incant! summons the best available token and dispatches
//!         let output = incant!(process_row(&input), [v3, neon, scalar]);
//!         (output, 256, 256)
//!     },
//!     &RegressionTolerance::off_by_one(),
//!     CompileTimePolicy::Warn,
//! );
//! ```
//!
//! `incant!` handles summoning and passing the right token type.
//! As `for_each_token_permutation` disables tokens, `incant!` falls back
//! to lower tiers automatically.
//!
//! ## Skipping crypto permutations
//!
//! On x86, crypto tokens (PCLMUL, AES) are independent from compute tiers
//! (AVX2, FMA). Image processing code never uses crypto instructions, so
//! toggling them just multiplies permutations without testing anything new.
//!
//! Pass `CryptoGrouping::Skip` to exclude crypto tokens from permutations:
//!
//! ```rust,ignore
//! check_simd_consistency_opts(
//!     || { ... },
//!     &RegressionTolerance::off_by_one(),
//!     CompileTimePolicy::Warn,
//!     CryptoGrouping::Skip,
//! );
//! ```
//!
//! | Grouping | On AVX2+AES machine | Permutations |
//! |----------|---------------------|-------------|
//! | `Clump` (default) | Compute permutations + one crypto-all-off run | ~5 |
//! | `Skip` | V1, V2, V3 only, crypto untouched | ~4 |
//! | `Combinatorial` | Full cross-product of all tokens | ~16 |
//!
//! ## `#[autoversion]` functions
//!
//! `#[autoversion]` generates everything you need — suffixed variants plus a
//! public dispatcher that hides the token:
//!
//! ```rust,ignore
//! #[autoversion]
//! pub fn blur_row(data: &[f32], kernel: &[f32], out: &mut [f32]) {
//!     // scalar loop — compiler auto-vectorizes per tier
//!     for i in 0..out.len() { ... }
//! }
//!
//! // Generated: blur_row_v4, blur_row_v3, blur_row_neon, blur_row_scalar
//! // Plus: blur_row() dispatcher (no token param, public API)
//! ```
//!
//! Test through the dispatcher:
//!
//! ```rust,ignore
//! check_simd_consistency(
//!     || {
//!         let mut out = vec![0.0f32; w * h * 3];
//!         blur_row(&input, &kernel, &mut out);  // dispatcher
//!         let rgba = f32_to_rgba(&out, w, h);
//!         (rgba, w as u32, h as u32)
//!     },
//!     &RegressionTolerance::off_by_one(),
//!     CompileTimePolicy::Warn,
//! );
//! ```
//!
//! ## Combining with oracle testing
//!
//! Oracle and SIMD consistency are complementary:
//!
//! - **Oracle** (in [`crate::oracle`]) verifies the output is *correct*
//!   (matches the mathematical definition).
//! - **SIMD consistency** verifies all tiers are *equivalent* (match each other).
//!
//! Use both. A bug where all tiers produce the same wrong answer passes SIMD
//! consistency but fails oracle. A bug where only one tier diverges might pass
//! oracle (depending on which tier ran) but fails SIMD consistency.
//!
//! # CI integration
//!
//! For full permutation coverage in CI, compile with the `testable_dispatch`
//! feature on archmage and use `CompileTimePolicy::Fail`:
//!
//! ```toml
//! [dev-dependencies]
//! archmage = { version = "0.9", features = ["testable_dispatch"] }
//! zensim-regress = { version = "0.2", features = ["archmage"] }
//! ```

